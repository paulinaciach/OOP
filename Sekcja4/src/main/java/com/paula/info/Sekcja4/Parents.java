package com.paula.info.Sekcja4;

public class Parents {
	
	private String momEyeColor;
	private String dadEyeColor;
	
	public Parents(String momEyeColor, String dadEyeColor) {
		this.momEyeColor = momEyeColor;
		this.dadEyeColor = dadEyeColor;
	}

	public String getMomEyeColor() {
		return momEyeColor;
	}

	public String getDadEyeColor() {
		return dadEyeColor;
	}
	
	
	
	
	
}
